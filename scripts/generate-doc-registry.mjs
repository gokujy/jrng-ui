import { readFile, writeFile } from 'node:fs/promises';
import fs from 'node:fs';
import { resolve } from 'node:path';
import { format } from 'prettier';

const workspace = resolve(import.meta.dirname, '..');
const inventory = JSON.parse(
  await readFile(resolve(workspace, 'docs/component-inventory.json'), 'utf8'),
);
const publicRegistry = JSON.parse(
  await readFile(resolve(workspace, 'projects/jrng-ui/registry/registry.json'), 'utf8'),
);
const publicBySelector = new Map(
  publicRegistry.components.map((component) => [component.selector, component]),
);
const records = inventory.components.map((component) => ({
  slug: component.selector.slice(2),
  name: component.name,
  className: component.className,
  selector: component.selector,
  category: component.category,
  importPath: component.publicImportPath,
  stability: titleCase(component.stability),
  description: technicalDescription(component),
  documentationStatus: component.documentationStatus,
  testStatus: component.testStatus,
  accessibilityStatus: component.accessibilityStatus,
  themeTokenSupport: component.themeTokenSupport,
  themeCoverage: component.themeCoverage,
  responsiveStatus: component.responsiveStatus,
  previewStatus: component.previewStatus,
  codeExampleStatus: component.codeExampleStatus,
  apiReferenceStatus: component.apiReferenceStatus,
  browserOnly: component.usesBrowserOnlyApis,
  optionalDependency: component.optionalExternalLibraries,
  inputs: publicBySelector.get(component.selector)?.inputs ?? [],
  outputs: publicBySelector.get(component.selector)?.outputs ?? [],
  methods: publicBySelector.get(component.selector)?.methods ?? [],
  keywords: [component.name, component.selector, component.category, component.publicImportPath],
}));

const source =
  `// Generated by scripts/generate-doc-registry.mjs. Do not edit manually.\n` +
  `export const generatedComponentRegistry = ${JSON.stringify(records, null, 2)} as const;\n`;
const output = resolve(workspace, 'projects/docs/src/app/docs/generated-component-registry.ts');
await writeFile(output, await format(source, { parser: 'typescript', singleQuote: true }));
console.log(`Generated documentation registry with ${records.length} public components.`);

const publicAudit = JSON.parse(
  await readFile(resolve(workspace, 'docs/audits/public-api-inventory.json'), 'utf8'),
);
const publicArtifacts = [];
const seenArtifacts = new Set();
for (const entrypoint of publicAudit.entrypoints.filter((item) => item.importPath !== 'jrng-ui')) {
  for (const artifact of entrypoint.artifacts ?? []) {
    if (!['directive', 'pipe', 'service', 'token'].includes(artifact.kind)) continue;
    const key = `${artifact.kind}:${artifact.originalName}`;
    if (seenArtifacts.has(key)) continue;
    seenArtifacts.add(key);
    publicArtifacts.push({
      name: displayName(artifact.name),
      identifier: artifact.name,
      selector: artifact.selector,
      importPath: artifact.entrypoint,
      kind: titleCase(artifact.kind),
      stability: 'Stable',
      description: artifactDescription(artifact),
      signatures: declarationSignatures(entrypoint.declaration, artifact.originalName),
      ssr: artifact.browserApis.length
        ? 'Feature-detected browser integration.'
        : 'SSR compatible.',
      cleanup: artifact.browserApis.length
        ? 'Lifecycle cleanup is required and covered by canonical tests.'
        : 'No browser resource cleanup required.',
    });
  }
}
const artifactSource =
  `// Generated by scripts/generate-doc-registry.mjs. Do not edit manually.\n` +
  `export const generatedPublicArtifactRegistry = ${JSON.stringify(
    publicArtifacts.sort((left, right) => left.name.localeCompare(right.name)),
    null,
    2,
  )} as const;\n`;
await writeFile(
  resolve(workspace, 'projects/docs/src/app/docs/generated-public-artifact-registry.ts'),
  await format(artifactSource, { parser: 'typescript', singleQuote: true }),
);
console.log(
  `Generated documentation registry with ${publicArtifacts.length} directives, pipes, services, and tokens.`,
);

function technicalDescription(component) {
  const capabilities = component.inputs.slice(0, 5).map(humanize);
  if (capabilities.length) {
    return `${component.name} configures ${joinList(capabilities)}.`;
  }
  return `${component.name} renders projected ${component.category.toLowerCase()} content using semantic JRNG tokens.`;
}

function artifactDescription(artifact) {
  if (artifact.kind === 'directive')
    return `${displayName(artifact.name)} adds typed host behavior through ${artifact.selector ?? 'an attribute selector'}.`;
  if (artifact.kind === 'pipe')
    return `${displayName(artifact.name)} transforms nullable display values through a pure Angular pipe.`;
  if (artifact.kind === 'service')
    return `${displayName(artifact.name)} coordinates library operations with SSR-safe fallbacks.`;
  return `${displayName(artifact.name)} configures a public JRNG provider contract.`;
}

function declarationSignatures(declaration, className) {
  if (!declaration) return [];
  const declarationPath = resolve(workspace, 'dist/jrng-ui', declaration);
  try {
    const text = requireText(declarationPath);
    const start = text.search(new RegExp(`declare class ${escapeRegExp(className)}\\b`));
    if (start < 0) return [];
    const end = text.indexOf('\ndeclare ', start + 10);
    return text
      .slice(start, end < 0 ? text.length : end)
      .split('\n')
      .map((line) => line.trim())
      .filter(
        (line) => /^(?:readonly\s+)?[A-Za-z_$][\w$]*(?:\??:|\()/.test(line) && !line.includes('ɵ'),
      )
      .slice(0, 20);
  } catch {
    return [];
  }
}

function requireText(file) {
  return fs.readFileSync(file, 'utf8');
}

function humanize(value) {
  return value.replace(/([a-z0-9])([A-Z])/g, '$1 $2').toLowerCase();
}
function displayName(value) {
  if (value.includes('_')) {
    return value.replace(/^J_?/, '').split('_').filter(Boolean).map(titleCase).join(' ');
  }
  return value
    .replace(/^Jr?/, '')
    .replace(/(Component|Directive|Pipe|Service)$/, '')
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2');
}
function titleCase(value) {
  return value.charAt(0).toUpperCase() + value.slice(1);
}
function joinList(values) {
  return values.length < 2 ? values[0] : `${values.slice(0, -1).join(', ')}, and ${values.at(-1)}`;
}
function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}
