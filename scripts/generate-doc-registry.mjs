import { readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { format } from 'prettier';

const workspace = resolve(import.meta.dirname, '..');
const inventory = JSON.parse(await readFile(resolve(workspace, 'docs/component-inventory.json'), 'utf8'));
const records = inventory.components.map((component) => ({
  slug: component.selector.slice(2),
  name: component.name,
  className: component.className,
  selector: component.selector,
  category: component.category,
  importPath: component.publicImportPath,
  stability: component.stability === 'Unclassified' ? 'Beta' : component.stability,
  description: `Angular ${component.name} component for reusable application interfaces.`,
  documentationStatus: component.documentationStatus,
  testStatus: component.testStatus,
  accessibilityStatus: component.accessibilityStatus,
  themeTokenSupport: component.themeTokenSupport,
  browserOnly: component.browserApiUsage,
  optionalDependency: component.optionalExternalLibraries,
  keywords: [component.name, component.selector, component.category, component.publicImportPath],
}));

const source = `// Generated by scripts/generate-doc-registry.mjs. Do not edit manually.\n` +
  `export const generatedComponentRegistry = ${JSON.stringify(records, null, 2)} as const;\n`;
const output = resolve(workspace, 'projects/docs/src/app/docs/generated-component-registry.ts');
await writeFile(output, await format(source, { parser: 'typescript', singleQuote: true }));
console.log(`Generated documentation registry with ${records.length} public components.`);
