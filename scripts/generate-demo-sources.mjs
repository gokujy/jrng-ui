import { readdir, readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { format } from 'prettier';

const workspace = resolve(import.meta.dirname, '..');
const demosRoot = resolve(workspace, 'projects/docs/src/app/demos');
const directories = (await readdir(demosRoot, { withFileTypes: true }))
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort();
const sources = {};

for (const directory of directories) {
  const base = resolve(demosRoot, directory, `${directory}.component`);
  sources[directory] = {
    html: await readNormalized(`${base}.html`),
    ts: await readNormalized(`${base}.ts`),
    scss: await readNormalized(`${base}.scss`),
  };
}

const output = resolve(demosRoot, 'demo-sources.generated.ts');
const source = `// Generated by scripts/generate-demo-sources.mjs. Do not edit manually.\nexport const demoSources = ${JSON.stringify(sources, null, 2)} as const;\n`;
await writeFile(output, await format(source, { parser: 'typescript', singleQuote: true }));
console.log(`Generated source tabs for ${directories.length} file-backed demos.`);

async function readNormalized(filePath) {
  return (await readFile(filePath, 'utf8')).replaceAll('\r\n', '\n');
}
